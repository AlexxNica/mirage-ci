open !Astring

let of_cstruct c =
  Cstruct.to_string c |>
  Sexplib.Sexp.of_string |>
  Dockerfile.t_of_sexp

let to_cstruct d =
  Dockerfile.sexp_of_t d |> fun s ->
  Sexplib.Sexp.to_string_hum s |> fun s ->
  Cstruct.of_string s

